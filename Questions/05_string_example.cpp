#include <iostream>
using namespace std;

int main() {

    // string is a built-in class used to store text
    string name = "Pawan Maurya";

    // Display string value
    cout << "Your name is : " << name << endl;

    // sizeof() gives memory size (in bytes) of the variable
    cout << "Size of name is : " << sizeof(name) << endl;

    return 0;
}
