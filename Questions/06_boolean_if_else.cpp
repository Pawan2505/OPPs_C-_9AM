#include <iostream>
using namespace std;

int main() {

    // bool datatype stores true or false
    bool isPresent = false;

    // if condition checks true/false value
    if (isPresent) {
        cout << "Ha Bhai Condition true ho gya hai" << endl;
    } else {
        cout << "Bhai false ho gya!" << endl;
    }

    return 0;
}
